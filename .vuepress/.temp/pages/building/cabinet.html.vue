<h2 id="cabinet-assembly" tabindex="-1"><a class="header-anchor" href="#cabinet-assembly"><span>Cabinet assembly</span></a></h2>
<p>We use 1.5&quot; x 1.5&quot; T-slotted profile to build the frame of the cabinet and 1/4&quot; black harcell ABS (smooth side up) to build the panels. Our supplier the Knotts Company, which is an authorized supplier of 80/20, and sent them the drawings for a quote. Another option is to buy the pieces described in the drawing from other supplier or suppliers, but it will require to do adjustments, and a lot of holes. Assemble the cabinet following the steps below.</p>
<ol>
<li>We start building the cabinet from the bottom up. Slide a pair of bottom plate holders (8020 - 2425) into the 24.5 inches long profile. Make sure the top part of the holder is at the same height as the top part of the profile, as shown in the picture below. Repeat the process for another 24.5&quot; long profile.</li>
</ol>
<figure>
  <img src='@source/building/assets/images/cabinet/cabinet-assembly-1.png'>
</figure>
<ol start="2">
<li>Insert a standard end fastener to both ends of the 24.5&quot; long profile with the bottom plate holders and to a pair of 22.5&quot; long profiles. First insert the fastener into one of the slots and loosely screw it, repeat the process with the other end making sure the direction of the fastener is the same. Make this for both ends of the 4 profiles.</li>
</ol>
<figure>
  <img src='@source/building/assets/images/cabinet/cabinet-assembly-2.png'>
</figure>
<ol start="3">
<li>Arrange the profiles in a square, making sure that the profiles with the plate holders are facing each other to the left and right, and in the correct position (top part of the holder must be at the same height as the top part of the profile); and the 22.5&quot; long profiles are fecing each other at the back and front. Use the 22.05&quot; long profile with holes at 1.5&quot; from the ends to join the front part, and use the 22.05&quot; long profile with holes at 1.5&quot; from both ends and a hole at 11.675&quot; from the top down to join the back part of the rig. Introduce the profiles from the top down and tight screw trough the holes.</li>
</ol>
