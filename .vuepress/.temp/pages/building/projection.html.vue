<h2 id="mirror" tabindex="-1"><a class="header-anchor" href="#mirror"><span>Mirror</span></a></h2>
<p>The spherical mirror is custom made from Thorlabs with the item number LA1740-P01-SP, be sure to contact them for a quote before polaceing the order and ask for the price breaks if you're planning to build more than 5 training rigs (it could go down up to 50% from 5 to 10 pieces).</p>
