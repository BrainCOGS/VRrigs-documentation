<h3 id="day-3-time-1-hour" tabindex="-1"><a class="header-anchor" href="#day-3-time-1-hour"><span>Day 3 [time: ~1 hour]</span></a></h3>
<ol>
<li>Apply All purpose foam coat + Bounce from Hot Wire Factory following the instructions for mixing (just make sure the mix is thin enough to handle but thick enough to work as a protective layer). Apply one coat to the exterior part of the sphere, if you find the layer to thin you can apply 2 layers. We recommend to first apply to the top part of the sphere from the aluminum plate, then the bottom part and finally the cutted edges of the sphere (everything but the inner surface of the sphere). Let it dry for 24 hrs.</li>
</ol>
 <figure>
   <img src='@source/building/assets/images/projection/screen-building-16.png'>
 </figure>
<h3 id="day-4-time-1-2-hour-each-layer-plus-drying-time" tabindex="-1"><a class="header-anchor" href="#day-4-time-1-2-hour-each-layer-plus-drying-time"><span>Day 4 [time: ~1/2 hour each layer plus drying time]</span></a></h3>
<ol>
<li>Apply white lightweight spackling to the inner surface of the sphere using a curved rubber wipe down knife, making sure cover the imperfections on the surface as much as you can. Let it dry for ~3 hours (or more depending on ambient temperature and humidity), you can touch the surface and it shouldn't stick to your hand. Sand the surface and apply another layer. Repeat the process from 2 to 3 times until you get a smooth surface on the screen.</li>
</ol>
 <figure>
   <img src='@source/building/assets/images/projection/screen-building-17.png'>
 </figure>
<h3 id="day-5-time-1-2-hour-each-layer-plus-drying-time" tabindex="-1"><a class="header-anchor" href="#day-5-time-1-2-hour-each-layer-plus-drying-time"><span>Day 5 [time: ~1/2 hour each layer plus drying time]</span></a></h3>
<ol>
<li>
<p>Paint the outer part of the screen with a black matte paint (we recommend the black paint on screen wall and ceiling ambien light rejectting acoustic dampening) using a 3&quot; or 4&quot; brush. Do not paint the edges of the screen yet. Apply 2 layers of paint to the outer part.</p>
</li>
<li>
<p>Use a roller to paint the inner part of the screen. Apply 1 layer of primer (we recommend to use the paint on screen leveling primer) and 2 to 3 layers of silver paint (we use the paint on screen S1 Screen Plus).</p>
</li>
<li>
<p>Using a 1&quot; brush paint the edges of the sphere with black matte paint.</p>
</li>
</ol>
