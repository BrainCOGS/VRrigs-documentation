</ol>
 <figure>
  <img src='@source/building/assets/images/projection/screen-building-9.png'>
 </figure>
<ol start="5">
<li>Place a mark at 2.5&quot; from the equator to the top of the ball along the 4.75&quot; and 6&quot; lines.</li>
</ol>
