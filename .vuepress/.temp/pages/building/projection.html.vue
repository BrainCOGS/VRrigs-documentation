<h2 id="screen-building" tabindex="-1"><a class="header-anchor" href="#screen-building"><span>Screen building</span></a></h2>
<p>The whole process of screen building can take up to 5 days. We describe the whole process in steps, adding which steps we make in a day and the time it took for us to follow the steps. We recommend to do it this way to allow the materials to properly dry. You could use the time it takes to dry to parallelize the work and assembly other modules in the meantime.</p>
<p>The first step for building the projection module consist on the fabrication of the dome screen. A styrofoam ball of 18&quot; outer diameter and 16&quot; inner diameter is being fabricated by an external provider. For ease of transportation the screens were sent as half spheres.</p>
<h3 id="day-1-time-3-hours" tabindex="-1"><a class="header-anchor" href="#day-1-time-3-hours"><span>Day 1 [time: ~3 hours].</span></a></h3>
<ol>
<li>If the balls are shipped in halfs, first mark the equator in each of the half spheres. Use a thread to measure the outer diameter of the screen (should be around 18&quot;) and place a mark at the middle (or around 9&quot;), place the thread at the bottom of the half sphere and mark at the sphere the joint of the thread and the middle (9&quot;) mark. We use a laser level to project a straight line through both marks and draw a line with a sharpie.</li>
</ol>
 <figure>
  <img src='@source/building/assets/images/projection/screen-building-1.png'>
 </figure>
<ol start="2">
<li>Use 3M Foam Fast 74 CA spray adhesive to glue both half spheres. Spray from 3 to 5 inches away both surfaces and wait for 1 minute, then attach both half spheres making sure to match the equator drawed lines, apply pressure on the union and leave it to dry and harden for at least 15 min.</li>
</ol>
 <figure>
  <img src='@source/building/assets/images/projection/screen-building-2.png'>
 </figure>
<ol start="3">
<li>Use lightweight joint compound to fill the line and the holes the adhesive might have caused, use a hand applicator to spread even on the surface trying to keep it as as smooth as possible. Let the joint compound dry from 30 min to 1 hour.</li>
</ol>
 <figure>
  <img src='@source/building/assets/images/projection/screen-building-3.png'>
 </figure>
<ol start="4">
<li>
<p>Place the aluminum plate in a flat surface making sure is hanging with enough space to fit the screen. Use a sharpie to mark a straigth line by joining the marks in the aluminum plate (placed at the middle of the circle) as seen in the pictures. Place the screen in the plate, making sure to align the equator with the plate and the meridian (the mark where the half spheres where joined) with the plate marked line.</p>
<div class="custom-container tip"><p class="custom-container-title">TIP</p>
<p>Sometimes the diameter of the balls is different from the plate, you can use some paper or carboard and tape it to fill the space between the screen and the plate for it to hang properly.</p>
</div>
</li>
</ol>
 <figure>
  <img src='@source/building/assets/images/projection/screen-building-4.png'>
 </figure>
<ol start="5">
<li>Glue about 5 inches of the sides and the back of the ball with an electric glue gun and wit for it to harden. Once cold, remove taped paper/cardboard and turn the plate around and glue the whole screen to the aluminum plate, wait for it to harden; turn once more time the plate and finish gluing the top part of the screen to the plate.</li>
</ol>
 <figure>
  <img src='@source/building/assets/images/projection/screen-building-5.png'>
 </figure>
<h3 id="day-2-time-3-hours" tabindex="-1"><a class="header-anchor" href="#day-2-time-3-hours"><span>Day 2 [time: ~3 hours]</span></a></h3>
<ol>
<li>
<p>Place the screen with the plate on a flat surface. We use a couple 14&quot; (1 inch diameter) pillars, other stuff could potentially be used just making sure the ball is freely hanguing from the plate and have enough space (at least ~6 inches) from the bottom of the screen to the flat surface. We place the pillars diagonally for stability.</p>
<p>Mark a line below 4.75&quot; from the equator of the screen. We use a set of Thorlabs BA2 and 1/2&quot; post (with a post holder) with a 90 degrees 1/2&quot; post holder to insert a sharpie marker as a tool to mark the line. We set the height of the post at 4.75&quot; below the equator and then mark the screen by moving the shapie along making sure the tool is flat on the surface.</p>
</li>
</ol>
 <figure>
  <img src='@source/building/assets/images/projection/screen-building-6.png'>
 </figure>
<ol start="2">
<li>Place a mark at the half of the scren across the meridian using the same tool as step 1 in day 1. The place the laser level in the flat surface facing the screen, use the vertical laser to throw a projection and align both marks at the half of the screen across the meridian. Mark a line from the top to the bottom of the screen (at least at the bottom line).</li>
</ol>
 <figure>
  <img src='@source/building/assets/images/projection/screen-building-7.png'>
 </figure>
<ol start="3">
<li>Measure from the joint of the two half spheres 4&quot; to the top and 4.75&quot; to the sides, place a mark and draw by hand a half circle using those 3 points.</li>
</ol>
