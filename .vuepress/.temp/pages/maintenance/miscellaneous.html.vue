<h3 id="wash" tabindex="-1"><a class="header-anchor" href="#wash"><span>Wash</span></a></h3>
<ol>
<li>Put some gloves on. Remove the print from the platform using a scraper -or just remove the quick release platform by pushing inwards both arms.</li>
<li>Rinse the print with IPA using a squeeze bottle. You can use compressed air to remove resin from concealed areas.</li>
<li>Open the Form Wash and place the print inside.</li>
<li>Select <a href="https://support.formlabs.com/s/article/Form-Wash-Time-Settings?language=en_US" target="_blank" rel="noopener noreferrer">resin-appropiate wash cycle<ExternalLinkIcon/></a> and start.</li>
<li>Rinse additional uncured resin using the squeeze bottle and let it dry for at least 30 minutes.</li>
</ol>
<h3 id="cure" tabindex="-1"><a class="header-anchor" href="#cure"><span>Cure</span></a></h3>
<ol>
<li>Once the print is completely dry, place it inside the Form Cure and select the <a href="https://support.formlabs.com/s/article/Form-Cure-Time-and-Temperature-Settings?language=en_US" target="_blank" rel="noopener noreferrer">appropiate curing settings<ExternalLinkIcon/></a>, then press start.</li>
<li>Once finished, allow the print to cool down to ambient temperature. This will avoid warping.</li>
<li>Remove the print and remove the supports.</li>
<li>Use sandpaper to remove any remaining suport marks.</li>
</ol>
