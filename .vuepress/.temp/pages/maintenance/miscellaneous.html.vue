<h3 id="print" tabindex="-1"><a class="header-anchor" href="#print"><span>Print</span></a></h3>
<ol>
<li>Export 3D print design from software as STL file and make sure to select the units.</li>
<li>Use PreForm to open the STL file, use the orientation tool and generate the support structures as needed.</li>
<li>Ensure that the build platform is clean and placed correctly. Properly insert the resin tank and resin cartridge - making sure the tap is open.</li>
<li>Upload the print.</li>
